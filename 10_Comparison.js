// Comparison

/*

== : equal
!= : not equal
> : greater than
< : leass than
>= : greater than or equal
<= : less than or equal

*/

let a = 5, b = 10;

console.log("a = ", a);
console.log("b = ", b);

console.log("is a equal b? : ", a == b);
console.log("is a not equal b? : ", a != b);
console.log("is a greater than b? : ", a > b);
console.log("is a less than b? : ", a < b);
console.log("is a greater than or equal b? : ", a >= b);
console.log("is a less than or equal b? : ", a <= b);