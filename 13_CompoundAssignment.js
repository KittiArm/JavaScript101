// Compound Assignment

/*

+= : 
-= : 
*= : 
/= : 
%= : 

*/

let x = 10, y = 20;
console.log("x =", x);
console.log("y =", y);

console.warn("x+=y");
console.log("Before x =", x);
console.log("After x =", x+=y);

console.warn("x-=y");
console.log("Before x =", x);
console.log("After x =", x-=y);

console.warn("x*=y");
console.log("Before x =", x);
console.log("After x =", x*=y);

console.warn("x/=y");
console.log("Before x =", x);
console.log("After x =", x/=y);

console.warn("x%=y");
console.log("Before x =", x);
console.log("After x =", x%=y);