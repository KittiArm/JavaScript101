// Variable type

/*

Variable type in JavaScript
1. boolean : True/False
2. number : integer (1, 10, 100), float (3.1416, 30.15)
3. string : "Kittipong"
4. object : {firstname:"Kittipong", lastname:"Chankhotr"};
5. array : ["Mango", "Papaya", "Orange"]


Function
1. typeof : for checking the type of variable
2. null : it haas not had value for variable yet, can set the value as null
3. undefuned : no value for variable yet

*/

// boolean
let _true = true;
let _false = false;
console.log(typeof(_true));
console.log(typeof(_false));

// number
let x = 20;
let y = 20.15;
console.log(typeof(x));
console.log(typeof(y));

// string
let _name = "Kittipong"
console.log(typeof(_name));

// object
let data = {firstname:"Kittipong", lastname:"Chankhotr"};
console.log(typeof(data));

// array
let fruit = ["Mango", "Papaya", "Orange"];
console.log(typeof(fruit));